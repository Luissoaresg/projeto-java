    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String strProduto = txtProduto.getText();
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        double valor = Double.parseDouble(txtValor.getText());
        
        Produto produto = new Produto();
        
        produto.setNomeProduto(strProduto);
        produto.setQuantidade(quantidade);
        produto.setValorProduto(valor);
        
        ProdutoDAO produtoDAO = new ProdutoDAO();
        if(produtoDAO.inserir(produto)){
            JOptionPane.showMessageDialog(null, "Produto " + strProduto + " cadastrado.");
            txtProduto.setText("");
            txtQuantidade.setText("");
            txtValor.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Produto n√£o cadastrado.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

