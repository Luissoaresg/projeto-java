    private void tblProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutosMouseClicked
        int linha = tblProdutos.getSelectedRow();
        if(linha != -1){
            DefaultTableModel tabelaProdutos = (DefaultTableModel) tblProdutos.getModel();
            
            int id = (int) tabelaProdutos.getValueAt(linha, 0);
            String produto = (String) tabelaProdutos.getValueAt(linha, 1);
            int quantidade = (int) tabelaProdutos.getValueAt(linha, 2);
            double valor = (double) tabelaProdutos.getValueAt(linha, 3);
            
            txtId.setText(String.valueOf(id));
            txtProduto.setText(produto);
            txtQuantidade.setText(String.valueOf(quantidade));
            txtValor.setText(String.valueOf(valor));
            
        }
    }//GEN-LAST:event_tblProdutosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo 'ID' n√£o esta preenchido, por favor selecione uma linha.");
            return;
        }
        
        ProdutoDAO produtoDAO = new ProdutoDAO();
        Produto p = new Produto();
        
        p.setId(Integer.parseInt(txtId.getText()));
        p.setNomeProduto(txtProduto.getText());
        p.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
        p.setValorProduto(Double.parseDouble(txtValor.getText()));
        
        if(produtoDAO.editar(p)){
            JOptionPane.showMessageDialog(null, "Produto editado com sucesso.");
            txtId.setText("");
            txtProduto.setText("");
            txtQuantidade.setText("");
            txtValor.setText("");
            
            preencherTabela();
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

