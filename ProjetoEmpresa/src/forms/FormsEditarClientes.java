    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int linha = tblClientes.getSelectedRow();
        if(linha != -1){
            DefaultTableModel tabelaClientes = (DefaultTableModel) tblClientes.getModel();
            
            int id = (int) tabelaClientes.getValueAt(linha, 0);
            String nome = (String) tabelaClientes.getValueAt(linha, 1);
            String cpf = (String) tabelaClientes.getValueAt(linha, 2);
            String contato = (String) tabelaClientes.getValueAt(linha, 3);
            String cidade = (String) tabelaClientes.getValueAt(linha, 4);
            String cep = (String) tabelaClientes.getValueAt(linha, 5);
            String veiculo = (String) tabelaClientes.getValueAt(linha, 6);
            
            txtId.setText(String.valueOf(id));
            txtNomeClientes.setText(nome);
            txtContato.setText(contato);
            txtCpf.setText(cpf);
            txtCidade.setText(cidade);
            txtCep.setText(cep);
            txtVeiculo.setText(veiculo);
            
        }
        
        
        
    }//GEN-LAST:event_tblClientesMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo 'ID' n√£o esta preenchido, por favor selecione uma linha.");
            return;
        }
        
        
        ClienteDAO clienteDAO = new ClienteDAO();
        Clientes cliente = new Clientes();
        
        cliente.setId(Integer.parseInt(txtId.getText()));
        cliente.setNomeCliente(txtNomeClientes.getText());
        cliente.setCpfCliente(txtCpf.getText());
        cliente.setContato(txtContato.getText());
        cliente.setCidade(txtCidade.getText());
        cliente.setCep(txtCep.getText());
        cliente.setVeiculo(txtVeiculo.getText());
        
        if(clienteDAO.editar(cliente)){
            JOptionPane.showMessageDialog(null, "Cliente editado com sucesso.");
            txtId.setText("");
            txtNomeClientes.setText("");
            txtCpf.setText("");
            txtContato.setText("");
            txtCidade.setText("");
            txtCep.setText("");
            txtVeiculo.setText("");
            
            preencherTabela();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

